# Operadores de Atribuição

# Esses operadores são utilizados para definir o valor inicial 
# ou sobrescrever o valor de de uma variável. 
# Note que podemos ter atribuição simples e também atribuições 
# envolvendo algum operador aritimetico.

# Atribuição simples
saldo = 500 
print(saldo)

# Atribuição com adição 
saldo = 500 
saldo += 200
print(saldo) 

# Atribuição com subtração
saldo = 200
saldo -= 100
print(saldo)

# Atribuição com multiplicação 
saldo = 900 
saldo *= 2 
print(saldo) 

# Atribuição com divisão 
saldo = 500 
saldo /= 5 
print(saldo)

# Divisão inteira
saldo = 500 
saldo //= 5 
print(saldo)

# Atribuição com módulo 
saldo = 500 
saldo %= 480 
print(saldo)

# Atribuição com exponenciação 
saldo = 80 
saldo **= 2 
print(saldo)

