# Operadores de Comparação

# São operadores usados para comparar dois valores.

# Igualdade
saldo = 450 
saque = 200 

print(saldo == saque)

# Note que o retorno é sempre um boleano true ou false

# Diferença 
print(saldo != saque)

# Maior que / maior ou igual
print(saldo > saque)
print(saldo >= saque)

# Menor que / menor ou igual
print(saldo < saque)
print(saldo <= saque)

# Mais alguns exemplos 
novo_saldo = 200
novo_saque = 200 

print(novo_saldo == novo_saque)
print(novo_saldo != novo_saque)
print(novo_saldo > novo_saque)
print(novo_saldo >= novo_saque)
print(novo_saldo <= novo_saque)
print(novo_saldo < novo_saque)